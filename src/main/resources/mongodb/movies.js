// AGRUPAR PREMIOS POR MOVIE
// ==========================================
const premiosPorMovie = {};

ganadores.forEach(([anyo, movieId, categoriaId, premioId]) => {
    if (!premiosPorMovie[movieId]) {
        premiosPorMovie[movieId] = [];
    }

    premiosPorMovie[movieId].push({
        premioId: premioId,
        titulo: premiosMap[premioId],
        categoriaId: categoriaId,
        categoria: categoriasMap[categoriaId],
        anyo: parseInt(anyo)
    });
});

// ==========================================
// UPDATE MOVIES CON PREMIOS
// ==========================================
Object.keys(premiosPorMovie).forEach(movieId => {
    db.movies.updateOne(
        { ID: parseInt(movieId) },
        {
            $set: {
                premios: premiosPorMovie[movieId]
            }
        }
    );
});

